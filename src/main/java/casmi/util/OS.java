package casmi.util;

/**
 * ENUM class for OS environment.
 * Expresses OS name and architecture.
 * 
 * @author T. Takeuchi
 * 
 */
public enum OS {

    /** Mac OS X 32bit. */
    MAC,
    
    /** Mac OS X 64bit. */
    MAC_64,
    
    /** Windows 32bit. */
    WIN,
    
    /** Windows 64bit. */
    WIN_64,
    
    /** Linux 32bit. */
    LINUX,
    
    /** Linux 64bit. */
    LINUX_64,
    
    /** Other OS. */
    OTHER
}
